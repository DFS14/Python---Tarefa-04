
#Programa que solicita ao usuário para digitar um número e informe se é par ou ímpar.

print('Digite um número:')
numero = int(input())


if   numero % 2 == 0: # Se o resto da divisão for 0, isso significa que numero é divisível por 2 e, portanto, é par.
    
    
     print('O número digitado é par.')
     
else:
    
     
     print('O número digitado é ímpar.')
     
     
   